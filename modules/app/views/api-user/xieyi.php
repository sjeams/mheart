一、我们如何收集儿童的个人信息
个人信息是指以电子或者其他方式记录的与已识别或者可识别的自然人有关的各种信息，不包括匿名化处理后的信息。心缘网络基于本《儿童个人信息保护政策》所述的以下目的，收集和使用儿童的以下个人信息。如需超过以下目的使用儿童的信息，心缘网络将另行征得监护人的同意或取得其他合法性依据：

1.1 儿童在使用我们服务时主动提供的信息
1.1.1 帮助儿童完成注册及登录
为帮助儿童注册、登录、使用心缘网络提供的服务，儿童主要会向我们提供的相关个人信息包括儿童的电话号码、电子邮箱、网络账号、密码、口令、昵称、头像等信息。
1.1.2 进行实名制认证
为满足相关法律法规政策及相关主管部门的要求，心缘网络用户需进行实名认证以继续使用和享受心缘网络。我们会在获得监护人同意或监护人主动提供的情况下收集儿童的实名身份信息，包括儿童的真实姓名、身份证号、生日、籍贯、性别、照片等，其中包含儿童的敏感个人信息，若儿童选择不提供实名身份信息，可能会导致儿童无法登录心缘网络的服务或在使用心缘网络的服务过程中受到相应限制。为实现实名认证的目的， 监护人同意并授权我们向有关实名认证机构（国家网络游戏主管部门、公安部门或者其委托/合作的其他机构）提供、查询、核对儿童的前述身份信息。
1.1.3 开展用户调研和活动
儿童通过我们的客服或参加我们举办的活动时所提交的信息。例如，儿童参与我们线上活动时填写的调查问卷中可能包含儿童的性别、教育程度、年龄等信息；儿童参与我们的包含赠品、奖品、生日祝福等增值服务，经监护人同意向我们提供的相关个人信息，包括姓名、电话、生日等。
1.2 我们在儿童使用我们服务时获取的信息
1.2.1 提供游戏基本功能
1.2.1.1 为了提供游戏服务的基本功能，我们会收集儿童的设备或软件信息，包括儿童的移动设备、网页浏览器或用于接入我们服务的其他程序所提供的配置信息、IP地址和终端设备（包括手机、平板电脑、电脑、 移动智能设备、家用游戏机、客户设备、无线路由器、操作系统及其他必备软、硬件或上网装置）所用的型号、设备识别码或设备 ID（MAC/IMEI/Android ID/IDFA/OpenUDID/GUID/SIM 卡IMSI 信息）、设备名称、设备类型和版本、设备分辨率、包名、设备设置、软硬件特征信息、系统版本、应用 ID、网络类型信息等。
1.2.1.2 我们会收集儿童的游戏日志信息，包括登录日志、物品日志、游戏对局信息、交友记录等，以便儿童能够在客户端查看游戏历史记录，同时用于游戏运营统计分析、客服投诉处理及其他游戏安全分析，并为提升儿童的游戏体验，我们可能把前述信息同步至该游戏后续版本或儿童正在使用的我们提供的其他服务中。
1.2.1.3 通过具有定位功能的移动设备，在儿童使用我们的服务时，我们可能通过GPS、基站、IP地址或WiFi等方式收集的儿童的地理位置信息；位置信息属于敏感个人信息，拒绝提供该信息只会导致儿童无法实现心缘网络服务对应的部分功能，但不影响儿童正常使用心缘网络服务的其他功能，儿童或监护人也可以随时取消儿童的地理位置信息授权。
1.2.2 帮助儿童完成下单、支付、充值并交付商品或服务
1.2.2.1 当儿童使用心缘网络服务的消费功能时，我们会收集儿童的充值记录、消费记录、订单号/流水号、订单创建时间、交易金额等信息，以便查询儿童的交易记录，同时尽最大程度保护儿童的虚拟物品安全。充值记录、消费记录属于敏感个人信息，我们收集该信息为实现心缘网络服务的消费功能所必须，否则将无法完成交易。
1.2.2.2 为完成订单支付或交付商品、服务、确认交易状态及为儿童或监护人提供售后与争议解决服务， 我们会通过儿童基于交易所选择的交易对象、支付机构、物流公司等，收集与交易进度相关的儿童的帐号、订单、交易、物流信息，或将儿童的交易信息共享给上述服务提供者。
1.2.3 维护网络及运营安全
1.2.3.1 为保障儿童的游戏账号安全，营造公平、健康及安全的游戏环境，我们会收集儿童的游戏识别信息、硬件及操作系统信息、进程及游戏崩溃记录等信息，以用于打击破坏游戏公平环境或干扰、破坏游戏服务正常进行的行为（如用于检测盗版、扫描外挂、防止作弊的目的）。
1.2.3.2 当儿童使用心缘网络服务的社交功能时，可以通过文字、图片、语音、视频及其他方式与其他玩家进行互动，我们可能会收集并保存儿童发送的上述信息内容用于过滤色情、暴力、政治、辱骂、恶意广告等不当内容，以此净化游戏环境，维护健康的上网环境。
1.2.4 个性化推荐
基于我们获得的儿童的个人信息（如儿童的设备信息、订单信息等），我们可能会基于上述一项或几项信息的结合，向儿童提供更契合个人需求的页面展示和推荐个性化内容及广告。如儿童或监护人不希望接受个性化推荐服务，可以在游戏服务的界面内查看管理方式并关闭相应功能。如对个性化推荐有任何问题，儿童或监护人可以通过本《儿童个人信息保护政策》下方提供的联系方式与我们进行联络。
