1.3 我们从第三方处间接获取的信息
我们可能从第三方获取儿童授权共享的账户信息（ID、账户头像、昵称），使儿童可以通过第三方账户进行登录。我们还可能从第三方社交平台获取儿童参与我们在第三方社交平台举办的互动、抽奖活动时授权该第三方共享的收货信息，以便我们寄送奖品。
如果我们从第三方处获取了儿童的个人信息，我们会按照与第三方的约定，在符合相关法律和法规规定的前提下，使用儿童的这些个人信息。
二、我们如何使用儿童的个人信息
2.1 我们会将前述收集的信息用于以下用途。若我们需要出于其他目的收集和/或使用儿童的个人信息的，我们会另行征求监护人的同意（如适用法律要求）：
2.1.1 帮助儿童注册心缘网络账户：为便于我们向儿童提供服务，需要收集真实、完整、准确、有效的个人身份信息及其他相关信息以完成实名账户的注册。
2.1.2 向儿童提供游戏服务并维护网络及运营安全：为了向儿童提供服务，或为了使儿童获得更优质的服务体验，我们将收集并使用儿童的必要个人信息。
2.1.3 产品开发和服务优化：在产品开发和服务优化的过程中，儿童的信息可能会被用于分析故障原因、分析用户需求、进行个性化定制、进行产品升级等。儿童在单项服务中的信息可能会被用于心缘网络提供的其他服务之中，以用于完善相关服务。
2.1.4 向儿童推送信息以及个性化推荐：
2.1.4.1 商品或服务信息：心缘网络会根据已收集的关于儿童的信息，如浏览及搜索记录、设备信息、位置信息，提取其浏览偏好、行为习惯、位置信息等特征，通过电子邮件、短信、通知等方式向儿童推送相关心缘网络服务或第三方服务的广告或资讯；如儿童或监护人不希望继续接收我们推送的消息，可要求我们停止推送，例如：根据短信退订指引要求我们停止发送推广短信，或在移动设备中进行设置，不再接收我们推送的消息；但我们依法律规定或服务条款约定发送消息的情形除外。
2.1.4.2 系统公告：在必要时（如心缘网络提供的服务需要进行维护、变更、中止或终止等），我们将利用所提供的信息向儿童或监护人发出系统公告
2.1.5 为儿童提供安全保障：为了保障儿童的账户安全，我们可能会将儿童的信息用于实名认证、身份验证、安全防范、交易风险防范、反诈骗监控、存档备份、客户服务等用途。在此过程中，我们可能会在必要的限度内向第三方提供儿童的信息，用以实现前述目的，并要求第三方遵守本《儿童个人信息保护政策》的规定。
2.1.6 其他用途：如果我们要将儿童的个人信息用于本《儿童个人信息保护政策》中未载明的其他用途，我们会通过页面提示、交互流程、网站公告或其他方式另行征得监护人的同意（如适用法律要求）。如果监护人同意或继续使用我们的服务，该等额外用途将视为本《儿童个人信息保护政策》的一部分。
2.2 根据相关法律法规规定，以下情形中收集和/或使用儿童的信息可能无需征得监护人的授权同意，但我们仍将根据适用法律法规的要求，竭力维护儿童个人信息安全并保障监护人的知情权和决定权：
2.2.1 为订立、履行个人作为一方当事人的合同所必需；
2.2.2 为履行法定职责或者法定义务所必需；
2.2.3 为应对突发公共卫生事件，或者紧急情况下为保护自然人的生命健康和财产安全所必需；
2.2.4 为公共利益实施新闻报道、舆论监督等行为，在合理的范围内处理个人信息；
2.2.5 依照适用法律规定在合理的范围内处理个人自行公开或者其他已经合法公开的个人信息；
2.2.6 法律、行政法规规定的其他情形。
三、我们如何使用Cookie和同类技术
3.1 为向儿童提供个性化的用户体验和服务，我们或我们的第三方合作伙伴可能会通过Cookie、脚本、Web信标、Flash Cookie、内嵌Web链接、本地存储器等工具自动进行信息收集，这些第三方Cookie或同类技术收集和使用该等信息不受本《儿童个人信息保护政策》约束，而是受到其自身的信息保护声明约束，我们不对第三方的Cookie或同类技术承担责任。
3.2 儿童和监护人可以通过浏览器或用户选择机制拒绝或管理Cookie及同类技术，但若儿童和监护人拒绝前述技术，则可能无法享受最佳的服务体验或无法享受特定服务的部分功能。
四、我们如何共享、转让、公开披露或委托处理儿童的个人信息
4.1 共享
心缘网络非常重视保护儿童的个人信息。 我们仅会出于合法、正当、必要、特定、明确的目的向第三方提供儿童的个人信息。对于接收我们提供个人信息的公司、组织和个人，我们会与其签署严格的保密协议，要求他们按照双方的约定、本《儿童个人信息保护政策》以及其他相关的保密和安全措施来处理信息。我们可能在以下情况下与我们以外的任何第三方共享儿童的个人信息：
4.1.1 在获取监护人的单独同意的情况下共享：在向监护人告知接收方的名称或者姓名、联系方式、处理目的、处理方式和个人信息的种类并获得监护人的单独同意后，我们会与其他方共享儿童的个人信息。
4.1.2 我们可能会根据法律法规规定，或按政府主管部门的强制性要求，对外共享儿童的个人信息。
4.1.3 与我们的关联公司共享：儿童的个人信息可能会与我们的关联公司共享。我们只会共享必要的个人信息，且受本《儿童个人信息保护政策》中所声明目的的约束。关联公司如要改变个人信息的处理目的，将再次征求监护人的授权同意。
4.1.4 与第三方SDK类服务商共享：仅为实现本《儿童个人信息保护政策》中声明的目的，我们的某些服务将由第三方SDK类服务商提供。我们可能会与第三方SDK类服务商共享儿童的某些个人信息，以提供更好的客户服务和用户体验。我们仅会出于合法、正当、必要、特定、明确的目的共享儿童的个人信息，并且只会共享提供服务所必要的个人信息。前述服务商收集和处理信息等行为遵守其自身的隐私条款，而不适用于本《儿童个人信息保护政策》。儿童或监护人可以通过下表心缘角网络游戏第三方SDK清单》了解我们接入第三方SDK的具体情况：
