五、我们如何存储和保护儿童的个人信息
5.1 信息存储：
5.1.1 存储地点：我们将根据相关法律法规的要求存储儿童的个人信息，在法律适用的情况下，对于我们在中华人民共和国境内收集的信息，我们将存储在中国境内。
5.1.2 存储期限：我们将按相关法律法规的要求，依据本《儿童个人信息保护政策》第二条目的得以实现的必要期限，来存储儿童的信息。
5.2 信息保护：
5.2.1 我们将采取符合法律要求及业界标准的、合理可行的安全防护措施保护儿童的个人信息， 防止数据遭到未经授权的访问、披露、使用、修改、损坏或丢失。依据实际情况，我们可能采取的安全防护措施包括使用加密技术（例如SSL、TLS）处理个人信息、对个人信息进行匿名化处理或脱敏处理、为相关网站提供 https 安全浏览方式、建立数据分类分级、安全管理、内部访问权限等制度、开展内部安全和隐私保护培训等。
5.2.2 若发生个人信息安全事件，我们将按照法律法规的要求，启动相应的应急预案，并及时向儿童和监护人告知安全事件的基本情况和可能的影响、我们已采取或将要采取的处置措施、儿童和监护人可自主防范和降低风险的建议、采取的补救措施等。我们将及时将事件相关情况以邮件、信函、电话、推送通知等方式告知儿童和监护人，难以逐一告知个人信息主体时，我们会采取合理、有效的方式发布公告。同时，我们还将按照监管部门要求，及时上报个人信息安全事件的处置情况。
