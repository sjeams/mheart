心缘角网络游戏儿童个人信息保护政策及监护人须知》（“《儿童个人信息保护政策》”）适用于成都心缘网络科技有限公司和/或其关联公司（统称“心缘网络”或“我们”）提供的游戏产品和服务（产品和服务统称为“服务”）。当儿童（指未满14周岁的未成年人）在使用心缘网络的服务时，心缘网络仅会在法律允许、监护人明确同意以及为儿童提供服务所必要的情况下收集、使用、存储、处理儿童的相关信息。心缘网络深知儿童个人信息安全的重要性，将尽全力保护儿童的信息安全与合法权益。

有关我们如何处理和保护已满14周岁用户的个人信息，请另行查看心缘角网络游戏个人信息保护政策》（ https://ak.hypergryph.com/protocol/privacy）。

请儿童的父母或其他监护人（以下简称 “监护人”）和您的孩子共同阅读并理解本《儿童个人信息保护政策》，特别是其中加粗的条款内容，并在充分理解并同意后使用我们的服务。

