儿童和监护人可以通过我们的服务主动公开并与第三人共享儿童的个人信息。请注意，只要不删除已公开发布的信息，相关信息将一直留存在公开领域；即便已删除公开信息，相关信息也可能被第三人缓存、收集、复制或向公众提供。请儿童和监护人谨慎考虑主动披露儿童的个人信息，以及由此可能导致的信息泄露风险，特别是敏感个人信息（如种族、宗教、个人健康和医疗信息、身份证明文件、个人生物识别信息、财产信息、行踪轨迹），儿童和监护人可以使用我们的相关设置（如有）来控制公开信息的范围。如儿童和监护人通过上述渠道公开儿童的个人信息，由此导致的信息泄漏，或对儿童、监护人或他人的损害，我们不承担任何责任。
6.3 要求更正或删除儿童的个人信息
若儿童和监护人发现我们处理的关于儿童的个人信息存在错误的，可以要求我们进行更正。
若儿童和监护人发现我们处理个人信息的行为违反法律法规、与儿童和监护人的约定、本《儿童个人信息保护政策》，或儿童不再使用我们的服务的或已注销了账号，儿童和监护人可以要求我们删除儿童的个人信息。
作为监护人，您理解依据相关法律法规的强制性规定，我们可能无法响应要求删除信息的特定要求。
作为监护人，您理解儿童的部分或全部个人信息被删除后，与此相关的我们的服务，或其对应部分可能无法使用。
6.4 改变监护人授权同意的范围 监护人可以通过我们公布的渠道和方式改变监护人授权我们收集、使用、存储的个人信息的范围。当监护人撤回授权后，我们将无法继续为儿童提供撤回授权所对应的服务，也不再处理儿童的相应信息。但监护人收回授权同意的决定，不会影响此前基于监护人的授权而开展的个人信息处理。
